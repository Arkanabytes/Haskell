--La función succ toma cualquier cosa que tenga definido un sucesor y devuelve ese sucesor. Como puedes ver, simplemente hemos separado el nombre de la función
--y su parámetro por un espacio. Llamar a una función con varios parámetros es igual de sencillo. Las funciones min y max toman dos cosas que puedan ponerse en 
--orden (¡cómo los números!) y devuelven uno de ellos.--

ghci> succ 8
